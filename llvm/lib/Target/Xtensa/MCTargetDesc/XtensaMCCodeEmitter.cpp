static void addFixup(SmallVectorImpl<MCFixup> &Fixups, uint32_t Offset,
                     const MCExpr *Value, uint16_t Kind) {
  bool PCRel = false;
  switch (Kind) {
  case Xtensa::fixup_xtensa_branch_6:
  case Xtensa::fixup_xtensa_branch_8:
  case Xtensa::fixup_xtensa_branch_12:
  case Xtensa::fixup_xtensa_jump_18:
  case Xtensa::fixup_xtensa_call_18:
  case Xtensa::fixup_xtensa_l32r_16:
  case Xtensa::fixup_xtensa_loop_8:
    PCRel = true;
  }
  Fixups.push_back(MCFixup::create(Offset, Value, Kind, PCRel));
}

struct AllowedSlots {
  bool Slot0 = false;
  bool Slot1 = false;
  bool Slot2 = false;
  bool Slot3 = false;
};

static AllowedSlots getAllowedSlots(const MCInst &Inst, const MCInstrInfo &MCII) {
  unsigned Opc = Inst.getOpcode();
  const MCInstrDesc &Desc = MCII.get(Opc);
  StringRef Name = MCII.getName(Opc);
  AllowedSlots Allowed;

  // HiFi instructions
  if (Name.starts_with("AE_")) {
    if (Name.starts_with("AE_ABS32_HIFI3") || Name.starts_with("AE_ABS64S_HIFI3") ||
        Name.starts_with("AE_ABSSQ56S_HIFI3") || Name.starts_with("AE_ADDSQ56S_HIFI3") ||
        Name.starts_with("AE_ADDSUB32_HIFI3") || Name.starts_with("AE_ADDSUB32S_HIFI3") ||
        Name == "AE_SLAI64S_HIFI3" || Name.starts_with("AE_SEXT32X2D16_10")) {
      Allowed.Slot0 = true;
      return Allowed;
    }
    if (Desc.mayLoad() && !Desc.mayStore()) {
      Allowed.Slot0 = true;
      Allowed.Slot1 = true;
      return Allowed;
    }
    if (Name.starts_with("AE_MUL")) {
      Allowed.Slot1 = true;
      return Allowed;
    }
    if (Name.starts_with("AE_ROUND") || Name.starts_with("AE_SAT") ||
        Name.starts_with("AE_CVT") || Name.starts_with("AE_SLAI") ||
        Name.starts_with("AE_SRAI") || Name.starts_with("AE_MAX") ||
        Name.starts_with("AE_MIN") || Name.starts_with("AE_ABS") ||
        Name.starts_with("AE_NEG") || Name.starts_with("AE_TRUNC") ||
        Name.starts_with("AE_PKSR") || Name.starts_with("AE_SEXT")) {
      Allowed.Slot3 = true;
      return Allowed;
    }
    if (Name.starts_with("AE_ADD32_HL_LH") || Name.starts_with("AE_ADD64S") ||
        Name.starts_with("AE_SUB24S")) {
      Allowed.Slot3 = true;
      return Allowed;
    }

    if (Name.starts_with("AE_ADD") || Name.starts_with("AE_SUB") ||
        Name.starts_with("AE_SEL") || Name.starts_with("AE_AND") ||
        Name.starts_with("AE_OR") || Name.starts_with("AE_XOR") ||
        Name.starts_with("AE_MOV") || Name.starts_with("AE_ZERO")) {
      Allowed.Slot1 = true;
      return Allowed;
    }
    Allowed.Slot0 = true;
    return Allowed;
  }

  // Scalar instructions
  switch (Opc) {
  // Memory (Strictly Slot 0)
  case Xtensa::L32I:
  case Xtensa::S32I:
  case Xtensa::L32R:
  case Xtensa::L8UI:
  case Xtensa::L16UI:
  case Xtensa::L16SI:
  case Xtensa::S8I:
  case Xtensa::S16I:
    Allowed.Slot0 = true;
    break;

  // ALU (Strictly Slot 1)
  case Xtensa::ADD:
  case Xtensa::ADDI:
  case Xtensa::SUB:
  case Xtensa::AND:
  case Xtensa::OR:
  case Xtensa::XOR:
  case Xtensa::MOVI:
  case Xtensa::NOP:
  case Xtensa::NEG:
  case Xtensa::ABS:
  case Xtensa::EXTUI:
  case Xtensa::SEXT:
  case Xtensa::CLAMPS:
  case Xtensa::MOVEQZ:
  case Xtensa::MOVNEZ:
  case Xtensa::MOVLTZ:
  case Xtensa::MOVGEZ:
  case Xtensa::ADDX2:
  case Xtensa::ADDX4:
  case Xtensa::ADDX8:
  case Xtensa::MIN:
  case Xtensa::MAX:
  case Xtensa::MINU:
  case Xtensa::MAXU:
  case Xtensa::SLLI:
  case Xtensa::SRLI:
  case Xtensa::SRAI:
  case Xtensa::SRC:
  case Xtensa::SSA8L:
  case Xtensa::SSA8B:
  case Xtensa::SSAI:
  case Xtensa::SSL:
  case Xtensa::SSR:
    Allowed.Slot1 = true;
    break;

  default:
    if (Desc.isConditionalBranch() || Desc.isUnconditionalBranch()) {
      Allowed.Slot0 = true;
    } else {
      Allowed.Slot0 = true;
    }
    break;
  }
  return Allowed;
}

static bool isBranchMCInst(const MCInst &Inst, const MCInstrInfo &MCII) {
  return MCII.get(Inst.getOpcode()).isConditionalBranch() ||
         MCII.get(Inst.getOpcode()).isUnconditionalBranch();
}

